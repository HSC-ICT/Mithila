// sub of 2 integer number
#include <stdio.h>
int main(){
    // Firstly we need to declare variables
    int a, b, sub;
    // assign value in those variables
    a = 5;
    b = 3;
    // calculate the sum
    sub = a - b;
    printf("%d",sub);
    // if we want to print something then
    // we need to use printf() function
    // the type of sub is integer and
    // it's format specifier is %d means decimal
    // must use quotation sign and the comma
    return 0;
}
